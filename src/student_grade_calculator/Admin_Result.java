    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
   try {
    String matricule = jTextField1.getText();
    String sql = "SELECT * FROM student WHERE Matricule = ?";
    pst = conn.prepareStatement(sql);
    pst.setString(1, matricule);
    ResultSet rs = pst.executeQuery();

    if (rs.next()) {
        // Matricule exists, allow filling other information and perform the insert
        String insertSql = "INSERT INTO marks(Matricule, java_programming_1, python_programming, software_engineering, computer_organisation, maths_statistics, technical_writing) VALUES (?, ?, ?, ?, ?, ?, ?)";
        pst = conn.prepareStatement(insertSql);
        pst.setString(1, matricule);
        pst.setString(2, jTextField2.getText());
        pst.setString(3, jTextField3.getText());
        pst.setString(4, jTextField4.getText());
        pst.setString(5, jTextField5.getText());
        pst.setString(6, jTextField6.getText());
        pst.setString(7, jTextField7.getText());
        int affectedRows = pst.executeUpdate();
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(null, "Successfully saved!");
        } else {
            JOptionPane.showMessageDialog(null, "Failed to save the data.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        // Matricule does not exist, display error message
        JOptionPane.showMessageDialog(null, "Wrong Matricule entered!", "Error", JOptionPane.ERROR_MESSAGE);
                    setVisible(false);
    new Admin_Result().setVisible(true);
    
    }
} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
} finally {
    try {
        if (pst != null) {
            pst.close();
        }
    } catch (SQLException e) {
        // Handle the exception if closing the prepared statement fails
    } 
}
    }//GEN-LAST:event_jButton6ActionPerformed

